#include "../hdrs/philo.h"

int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	ft_strrlen(const char **strr)
{
	int	i;

	i = 0;
	while (strr[i])
		i++;
	return (i);
}
